    {
        $fnl = \DB::table('fnlyear')->first();
        $gp = \DB::select('SELECT * FROM (stocks INNER JOIN sales ON stocks.ss_ID = sales.sl_i_ID) WHERE fyear = ?', [$fnl->fn_name]);
        $return = \DB::select('SELECT * FROM (stocks INNER JOIN salereturns ON stocks.ss_ID = salereturns.slr_i_ID) WHERE fyear = ?', [$fnl->fn_name]);
        return view('grossprofits.index', ['gp' => $gp, 'return' => $return]);
    }

